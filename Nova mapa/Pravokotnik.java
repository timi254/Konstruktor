public class Pravokotnik extends Stirikotnik{
    int a,b;

    public Pravokotnik(int xx, int yy, String bb){
        super(xx,yy,ba);  //klic konstruktorja nadrazreda
        this.a=aa;
        this.b=bb;
    }
    public void izpisPloscine(){
        System.out.println("Ploščina je: "+this.a*this.b);
    }
}
